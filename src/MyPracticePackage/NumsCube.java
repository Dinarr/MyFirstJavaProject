package MyPracticePackage;

import java.util.Scanner;

public class NumsCube {

	public static void main(String[] args) {
		cube();
	}
	
	
	public static void cube() {
		Scanner sc = new Scanner(System.in);
		int n = sc.nextInt();
		int cube = n*n*n;
		System.out.println(cube);
		
		
		
	}
	
	
	
	
	
}
